package modulo;

public class ModuloTesting {

    public static void main(String[] args) {

        int x = 7;
        int y = 3;
        int sx = -7;
        int sy = -3;


        System.out.println("x/y: "+x/y);
        System.out.println("sx/y: "+sx/y);
        System.out.println("sx/sy: "+sx/sy);
        System.out.println("x/sy: "+x/sy);


        System.out.println("x%y: "+x%y);
        System.out.println("sx%y: "+sx%y);
        System.out.println("sx%sy: "+sx%sy);
        System.out.println("x%sy: "+x%sy);


    }

}
